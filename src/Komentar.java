public class Komentar {
    public static void main(String[] args) {
        System.out.println(sum(15, 15));
    }

    /**
     * Method untuk menjumlahkan nilai 1 dan 2
     * @param nilai1 nilai 1
     * @param nilai2 nilai 2
     * @return penjumlahan dari nilai 1 dan 2
     */

    static int sum(int nilai1, int nilai2){
        //menjumlahkan nilai 1 dan nilai 2
        return nilai1+nilai2;
    }
}
